def test():
    root = TreeNode(1)
    root.left = TreeNode(2)
    root.right = TreeNode(3)
    root.left.right = TreeNode(5)
    s = Solution()
    print(s.binaryTreePaths(root))

if __name__ == '__main__':
    test()
